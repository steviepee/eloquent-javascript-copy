function isEven(num) {
  if (num < 0) {
    num *= -1;
    }
  if (num === 0) {
      return true;
    }
  if (num === 1) {
      return false;
    }
    //subtract 2 from num until it resolves to 0 or 1
  num = num - 2;  
  return isEven(num);
  }

