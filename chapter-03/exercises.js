let countChars = function(string, letter) {
  //have a variable represent the count. Start at 0;
  let count = 0
  //have the function iterate over the string
  for (let i = 0;  i < string.length; i++) {
    //if the letter is the same as the test letter
    if (string[i] === letter) {
      //add 1 to the count
      count += 1;
      }
      }//when the loop is done, return the count
    return count;
    }

