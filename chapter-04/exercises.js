function prepend(element, list) {
  return {value : element, rest: list};
  }


////////////////////////////////////////////////////////////////////////////////
// nth /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function nth(number, list, counter=0) {
  if (list.rest === null) {
    if (counter === number) {
      return list.value;
      }
    return undefined
    }
  if (counter === number) {
    return list.value
    } else {
    counter += 1;
    }
  return nth(number, list.rest, counter);
};

