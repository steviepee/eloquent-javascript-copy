// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(array) {
output = array.reduce(function (end, now) {return end += now}, 0);
return output;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(array) {
output = [];
array.map(function(value) {output.unshift(value)});
return output;

}

////////////////////////////////////////////////////////////////////////////////
// reverseArrayInPlace /////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArrayInPlace(array, input=[]) {
  if (array.length === 0) {
    array.push(input)
    return array;
  }
  input.unshift(array[0]);
  return reverseArrayInPlace(array.slice(1), input);
  

}

////////////////////////////////////////////////////////////////////////////////
// arrayToList /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

let arrayToList = array => {
  let rest = null;
  for (let i = array.length - 1; i >= 0; i--) {
    rest = {value: array[i], rest:rest};
    }
  return rest;
}

////////////////////////////////////////////////////////////////////////////////
// listToArray /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function listToArray(list, output=[]) {
  if (list.rest === null) {
    output.push(list.value);
    return output;
    }
  output.push(list.value);
  return listToArray(list.rest, output);
};

